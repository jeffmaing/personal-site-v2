// 关键词匹配函数
export function matchQA(userInput: string): QAEntry | null {
  const input = userInput.toLowerCase()
  for (const qa of qaDatabase) {
    if (qa.keywords.some(kw => input.includes(kw))) {
      return qa
    }
  }
  return null
}

